    void xoaTaBle(){
        while(dtm.getRowCount()>0)
            dtm.removeRow(0);
    }
    void hienThiThongTinTable(ArrayList<LoaiXe>a){
        xoaTaBle();
        for(LoaiXe x:a)
            if(x instanceof XeOto)
                dtm.addRow(new Object[]{x.getMaVe(),x.getNgayGui(),x.getSoLuong(),x.getDonGia(),((XeOto) x).getLoaiVe(),((XeOto) x).tinhThanhTien()});
            else 
                dtm.addRow(new Object[]{x.getMaVe(),x.getNgayGui(),x.getSoLuong(),x.getDonGia(),((XeMay) x).getKhuVuc(),((XeMay) x).tinhThanhTien()});
    }

