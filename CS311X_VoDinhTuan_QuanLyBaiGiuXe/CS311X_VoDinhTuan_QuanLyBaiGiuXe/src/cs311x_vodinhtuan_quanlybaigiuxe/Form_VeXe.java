    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ma, ngay, loaixe;
        double sl, dg;
        ma = txtMaVe.getText();
        ngay = txtNgayGui.getText();
        sl = Double.parseDouble(txtSoLuong.getText());
        dg = Double.parseDouble(txtDonGia.getText());
        loaixe = txtLoaiXe.getText().trim();
        LoaiXe x;
        if(loaixe.equalsIgnoreCase("VT") || loaixe.equalsIgnoreCase("VN"))
            x = new XeOto(loaixe, ma, ngay, dg, sl);
        else x= new XeMay(loaixe, ma, ngay, dg, sl);
        if(a.themMoi(x) == true)
            hienThiThongTinTable(a.getA());
        else JOptionPane.showMessageDialog(null, "Không thêm vì đã có mã " + ma + " trong danh sách " );
    }//GEN-LAST:event_btnThemActionPerformed

