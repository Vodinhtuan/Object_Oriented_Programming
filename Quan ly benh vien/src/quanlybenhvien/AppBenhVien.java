    void HienThiBN(int index) {
        if (index == -1 || index == danhsachBN.getDanhSach().size() + 1) {
            index = danhsachBN.getDanhSach().size() + 1;
            rbBHYT.setSelected(true);
            rbKhong.setSelected(true);
            tfMa.setText("");
            tfNgaynhap.setText("");
            tfNgayxuat.setText("");
            tfTen.setText("");
            tfSTT.setText(index + "");
        } else if (index > 0 && index <= danhsachBN.getDanhSach().size()) {
            if (danhsachBN.getDanhSach().get(index - 1) instanceof BENHNHANBAOHIEMYTE) {
                rbBHYT.setSelected(true);
            } else {
                rbBHXH.setSelected(true);
            }
            if (danhsachBN.getDanhSach().get(index - 1).isPhongyeucau()) {
                rbCo.setSelected(true); 
            } else {
                rbKhong.setSelected(true);
            }
            tfMa.setText(danhsachBN.getDanhSach().get(index - 1).getMa());
            tfNgaynhap.setText(danhsachBN.getDanhSach().get(index - 1).getNgaynhap() + "");
            tfNgayxuat.setText(danhsachBN.getDanhSach().get(index - 1).getNgayxuat() + "");
            tfTen.setText(danhsachBN.getDanhSach().get(index - 1).getHoten());
            tfSTT.setText(index + "");
            localbn = index;
        }
        CheckTTBN();
    }
    void HienThiNV(int index) {
        if (index == -1 || index == danhsachNV.getDanhSach().size() + 1) {
            index = danhsachNV.getDanhSach().size() + 1;
            rbNam.setSelected(true);
            cbChucvu.setSelectedIndex(0);
            tfMa1.setText("");
            tfNgayKHD.setText("");
            tfMucthuong.setText("");
            tfTen1.setText("");
            tfSTT1.setText(index + "");
        } else if (index > 0 && index <= danhsachNV.getDanhSach().size()) {
            if (danhsachNV.getDanhSach().get(index - 1).isGioitinh()) {
                rbNam.setSelected(true);
            } else {
                rbNu.setSelected(true);
            }
            cbChucvu.setSelectedIndex(danhsachNV.getDanhSach().get(index - 1).getChucvu());
            
            tfMa1.setText(danhsachNV.getDanhSach().get(index - 1).getMa());
            tfNgayKHD.setText(danhsachNV.getDanhSach().get(index - 1).getNgaykyhopdong() + "");
            tfMucthuong.setText(danhsachNV.getDanhSach().get(index - 1).getMucthuong() + "");
            tfTen1.setText(danhsachNV.getDanhSach().get(index - 1).getHoten());
            tfSTT1.setText(index + "");
            localnv = index;
        }
        CheckTTNV();
    }
    void NapDLBN() {
        int STT = 1;
        if (danhsachBN.CheckDL() == false) {
            JOptionPane.showMessageDialog(rootPane, "Khong co du lieu!!!");
            return;
        }
        tableBN.setRowCount(0);
        for (BENHNHAN i : danhsachBN.getDanhSach()) {
            tableBN.addRow(new Object[]{STT, i.getMa(), i.getHoten(), (i.isPhongyeucau() ? "Có" : "Không"), i.getNgaynhap(),
                i.getNgayxuat(), (i instanceof BENHNHANBAOHIEMYTE ? "BHYT" : "BHXH"), i.Tinhhoadonvienphi()});
            STT++;
        }
        CheckTTBN();
    }
    void NapDLNV() {
        int STT = 1;
        if (danhsachNV.CheckDL() == false) {
            JOptionPane.showMessageDialog(rootPane, "Khong co du lieu!!!");
            return;
        }
        tableNV.setRowCount(0);
        for (NHANVIEN i : danhsachNV.getDanhSach()) {
            tableNV.addRow(new Object[]{STT, i.getMa(), i.getHoten(), (i.isGioitinh() ? "Nam" : "Nữ"), i.getNgaykyhopdong(),
                i.getMucthuong(),cbChucvu.getItemAt(i.getChucvu()), i.TinhTienLuong()});
            STT++;
        }
        CheckTTNV();
    }
    
    void CheckTTBN() {
        if (danhsachBN.CheckDL() == false) {
            btCapnhat.setEnabled(false);
            tfSTT.setEnabled(false);
            btXoa.setEnabled(false);
            btTimkiem.setEnabled(false);
            btTong.setEnabled(false);
            btDau.setEnabled(false);
            btLui.setEnabled(false);
            btTien.setEnabled(false);
            btCuoi.setEnabled(false);
            btGiam.setEnabled(false);
            btTang.setEnabled(false);
            btMacdinh.setEnabled(false);
        }
        if (localbn == 1) {
            btLui.setEnabled(false);
            btDau.setEnabled(false);
        } else {
            btLui.setEnabled(true);
            btDau.setEnabled(true);
        }
        if (localbn == danhsachBN.getDanhSach().size() || localbn == danhsachBN.getDanhSach().size() + 1) {
            btCuoi.setEnabled(false);
        } else {
            btCuoi.setEnabled(true);
        }
        if (localbn == danhsachBN.getDanhSach().size() + 1) {
            btTien.setEnabled(false);
        } else {
            btTien.setEnabled(true);
        }

        if (tfMa.getText().equals("") || tfTen.getText().equals("") || tfNgaynhap.getText().equals("")
                || tfNgayxuat.getText().equals("")) {
            btCapnhat.setEnabled(false);
            btThem.setEnabled(false);
        } else {
            if (localbn < danhsachBN.getDanhSach().size() + 1) {
                btThem.setEnabled(false);
            } else {
                btThem.setEnabled(true);
            }
            btCapnhat.setEnabled(true);
        }
        
    }
