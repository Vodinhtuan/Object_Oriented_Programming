    private void btCapnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapnhatActionPerformed
        // TODO add your handling code here:
        try {
            if (rbBHYT.isSelected()) {
                if (!danhsachBN.CapNhat(new BENHNHANBAOHIEMYTE(tfMa.getText(), tfTen.getText(),
                        new NgayThang(tfNgaynhap.getText()), new NgayThang(tfNgayxuat.getText()), rbCo.isSelected()))) {
                    int option = JOptionPane.showConfirmDialog(rootPane, "Mã chưa có tự động thêm mới không!!!");
                    if (option == 0) {
                        ThemMoiBN();
                        localbn = danhsachBN.getDanhSach().size();
                        HienThiBN(localbn);
                    } else if (option == 1) {
                        HienThiBN(localbn);
                    }
                }

            } else {
                if (!danhsachBN.CapNhat(new BENHNHANBAOHIEMXAHOI(tfMa.getText(), tfTen.getText(),
                        new NgayThang(tfNgaynhap.getText()), new NgayThang(tfNgayxuat.getText()), rbCo.isSelected()))) {
                    int option = JOptionPane.showConfirmDialog(rootPane, "Mã chưa có tự động thêm mới không!!!");
                    if (option == 0) {
                        ThemMoiBN();
                        localbn = danhsachBN.getDanhSach().size();
                        HienThiBN(localbn);
                    } else if (option == 1) {
                        HienThiBN(localbn);
                    }
                }
            }
            ModeBN(modeBN);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e,"!!!WARNING!!!",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btCapnhatActionPerformed

